<h1>Adatvédelmi irányelvek</h1>

<p>A Chromium Network tiszteletben tartja a felhasználók magánéletét, és elkötelezett a felhasználók által velünk megosztott személyes adatok védelme mellett. Ez az adatvédelmi szabályzat elmagyarázza, hogy a Chromium Network hogyan gyűjti, használja és osztja meg a személyes adatokat.</p>

<h2>Milyen személyes adatokat gyűjt a Chromium Network?</h2>

<p>A Chromium Network többféle módon gyűjt személyes adatokat a felhasználóktól. Amikor a felhasználók fiókot hoznak létre a Chromium Networkön, gyűjtjük a felhasználónevüket, e-mail címüket és jelszavukat. A felhasználók IP-címét és böngészőinformációit is gyűjtjük, amikor meglátogatják a weboldalunkat.</p>

<h2>Hogyan használja fel a Chromium Network a személyes adatokat?</h2>

<p>A Chromium Network a személyes adatokat arra használja fel, hogy a felhasználók számára az általuk kért szolgáltatásokat nyújtsa. Például a felhasználók felhasználónevét és e-mail címét arra használjuk, hogy fiókinformációkat és értesítéseket küldjünk nekik. A felhasználók IP-címét és a böngészőre vonatkozó információkat is felhasználjuk, hogy segítsünk a weboldalunk és szolgáltatásaink javításában.</p>

<h2>Megosztja a Chromium Network a személyes adatokat harmadik felekkel?</h2>

<p>A Chromium Network nem osztja meg személyes adatait harmadik felekkel marketingcélokra. Megoszthatunk azonban személyes adatokat olyan harmadik felekkel, akik szolgáltatásokat nyújtanak számunkra, például a weboldalunk tárhelyét. Ezek a harmadik felek szerződéses kötelezettséget vállalnak arra, hogy a felhasználók személyes adatait bizalmasan és biztonságosan kezelik.</p>

<h2>Hogyan férhetnek hozzá, javíthatják vagy törölhetik személyes adataikat a felhasználók?</h2>

<p>A felhasználók személyes adataikhoz hozzáférhetnek, kijavíthatják vagy törölhetik azokat, ha bejelentkeznek fiókjukba, és a "Profil" oldalra lépnek. A felhasználók Gyurkovics Dominikhoz is fordulhatnak a gydoma@chromiumnetwork.com e-mail címen, hogy hozzáférést kérjenek személyes adataikhoz, kijavítsák vagy töröljék azokat.</p>

